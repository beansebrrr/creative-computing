"""
Relational / Comparison Operator
"""

print("\n$========o Comparison Operator o========$\n")

num1 = int(input("Enter your first number: "))
num2 = int(input("Enter your second number: "))

print("\n$=======================================$\n")

# Prints out boolean (True or False):
print(num1 > num2)      # If 'num1' is greater than 'num2'
print(num1 < num2)      # If 'num1' is less than 'num2'
print(num1 >= num2)     # If 'num1' is greater than or equal to 'num2'
print(num1 <= num2)     # If 'num1' is less than or equal to 'num2'
print(num1 == num2)     # If 'num1' is equal to 'num2'
print(num1 != num2)     # If 'num1' is not equal to 'num2'

print("\n$=======================================$\n")