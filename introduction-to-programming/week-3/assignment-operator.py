"""
Assignment Operator
"""

print("\n$========o Assignment Operator o========$\n")

# Asks for variable 'a'
a = int(input("Give me a number: "))

print("\n$=======================================$\n")

# Adds 5 to 'a'
a += 5
print(a)

# Subtracts 4 from 'a'
a -= 4
print(a)

# Multiplies 'a' by 5
a *= 5
print(a)

# Divides 'a' by 4
a /= 4
print(a)

print("\n$=======================================$\n")