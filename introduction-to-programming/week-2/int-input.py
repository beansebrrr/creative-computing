# Gets input from the user via the terminal
num1 = int(input("Give me the first number: "))
num2 = int(input("Now give me the second number: "))
num3 = int(input("Now the third and last number: "))

# Assigns the product of the 3 numbers into a separate variable
product = num1 * num2 * num3

print(f"{num1} * {num2} * {num3} = {product}")
